// Helper functions
export function getHospitalSpecialties(hospitalSlug) {
    return hospitalSpecialties[hospitalSlug] || [];
}

export function getSpecialtyByHospitalAndId(hospitalSlug, specialtyId) {
    const specialties = getHospitalSpecialties(hospitalSlug);
    return specialties.find(specialty => specialty.id === specialtyId);
}

export function getAllHospitalSpecialties() {
    return hospitalSpecialties;
}

// Navigation helper
export function createBookingUrl(hospitalSlug, specialtyId, ageGroupId = null) {
    let url = `booking.html?hospital=${hospitalSlug}&specialty=${specialtyId}`;
    if (ageGroupId) {
        url += `&age=${ageGroupId}`;
    }
    return url;
}